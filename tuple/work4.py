"""
Write a program to unpack the following tuple into four variables and display each variable.

tuple1 = (10, 20, 30, 40)
"""

tuple1 = (10, 20, 30, 40)

variable1 = tuple1[0]
variable2 = tuple1[1]
variable3 = tuple1[2]
variable4 = tuple1[3]

print(variable1)
print(variable2)
print(variable3)
print(variable4)


# OR

a, b, c, d = tuple1

print(a)
print(b)
print(c)
print(d)